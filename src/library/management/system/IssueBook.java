    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
          if(txtbookid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All Fields are required!");
            txtbookid.requestFocus();
        }else if(studentid.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All Fields are required!");
            studentid.requestFocus();
        }else if(duedate.getText().equals("")){
            JOptionPane.showMessageDialog(this, "All Fields are required!");
            duedate.requestFocus();
        }else {
            try {

                pst = con.prepareStatement("UPDATE book SET status =?,issuedate=? , duedate=? , studentid=? where id =?");
                pst.setString(1, "ISSUED");
                pst.setString(2, issuedate.getText());
                pst.setString(3, duedate.getText());
                pst.setString(4, studentid.getText());
                pst.setString(5, txtbookid.getText());
               
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Book issued successfully");
                txtbookid.setText("");
                studentid.setText("");
                txtbookname.setText("");
                issuedate.setText("");
                duedate.setText("");
//
            } catch (SQLException ex) {
                Logger.getLogger(StudentRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_btnsaveActionPerformed

